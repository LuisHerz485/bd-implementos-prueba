CREATE TABLE Productos(
	ID BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    COD VARCHAR(20) UNIQUE,
    NOMBRE VARCHAR(200),
    UNITID VARCHAR(6),
    PRECIO_VENTA DECIMAL(20,2),
    COSTO_FINANIERO DECIMAL(20,20),
    ID_LINEA VARCHAR(20),
    ID_CATEGORIA VARCHAR(20),
    COD_ID_PROVEEDOR VARCHAR(60),
    ESTADO VARCHAR(1),
    TIPO_FABRICA VARCHAR(1),
    NUMERO_PARTE VARCHAR(40),
    LINEA VARCHAR(100),
    CATEGORIA VARCHAR(100),
    UEN VARCHAR(100),
    MARCA VARCHAR(100),
    PRECIO_MINIMO DECIMAL(20,2),
    DESCRIPCION VARCHAR(255),
    IMAGEN_150 VARCHAR(255),
    IMAGEN_450 VARCHAR(255)
)